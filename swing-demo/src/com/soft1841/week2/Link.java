package com.soft1841.bookswing.week2;

/**
 * 连接其他数据类型
 * 将字符串与数字连接
 * @author 刘恋
 * 2019.3.24
 */

public class Link {
    public static void main(String[] args) {
        // 声明的int型变量booktime
        int booktime = 4;
        // 声明的float型变量practice
        float practice = 2.5f;
        // 将字符串与整型，浮点型变量连接并将其输出
        System.out.println("我每天花费" + booktime + "小时看书：" + practice + "小时上机练习");
    }
}
